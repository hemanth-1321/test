def sum(a, b):
    """Calculate the sum of two numbers and print the result."""
    result = a + b
    print(result)

def mutlipy(a, b):
    """Return the product of two numbers."""
    return a * b

if __name__ == "__main__":
    # Example usage (optional)
    print("Sum of 3 and 4:", end=" ")
    sum(3, 4)
    print("3 multiplied by 5:", mutlipy(3, 5))